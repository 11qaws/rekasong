function documentLocale() {
  if (typeof document === 'undefined') return 'ko';
  return document.documentElement.lang || 'ko';
}

export function getAppMessage(key, values = {}, locale = documentLocale()) {
  const normalized = String(locale || 'ko').toLowerCase().split('-')[0];
  const template = appMessageCatalog[normalized]?.[key]
    ?? appMessageCatalog.ko[key];
  if (template === undefined) return getOutputMessage(key, values, locale);
  return String(template).replace(/\{\{(\w+)\}\}/g, (placeholder, variableName) => (
    Object.prototype.hasOwnProperty.call(values, variableName)
      ? String(values[variableName])
      : placeholder
  ));
}
